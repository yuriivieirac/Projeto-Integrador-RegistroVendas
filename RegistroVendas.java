    private void butonPesquisarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butonPesquisarProdutoActionPerformed
    
        conex.conexão();//faz conexão

        try {
            PreparedStatement pst = conex.con.prepareStatement("insert into tbvenda(valor_venda)values(?)");

            pst.setFloat(1, 0);
            pst.execute();
            conex.execultaSql("select * from tbvenda");
            conex.rs.last();
            codigovenda = conex.rs.getInt("id_venda");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro: " + ex);
        }

        CampoCliente.setEnabled(false);
        ButtonPesquisarCliente.setEnabled(false);
            flag = 2;
        preencherTabelaProduto("select * from tbprodutos Where nome_produto like'%" + campoProduto.getText() + "%'");
        campoProduto.setText("");
        conex.desconecta();
    }//GEN-LAST:event_butonPesquisarProdutoActionPerformed

    private void tabelaPesquisaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaPesquisaMouseClicked
        if (flag == 1) {
            String nome_cliente = "" + tabelaPesquisa.getValueAt(tabelaPesquisa.getSelectedRow(), 1);
            CampoCliente.setText(nome_cliente);
        } else {
            conex.conexão();
            try {
                String nome_produto = "" + tabelaPesquisa.getValueAt(tabelaPesquisa.getSelectedRow(), 1);
                conex.execultaSql("select * from tbprodutos where nome_produto='" +nome_produto+ "'");
                conex.rs.first();

                precoProduto = conex.rs.getFloat("preco_venda");//conect com BD
                campoProduto.setText(nome_produto);
                CampoValorItem.setText(String.valueOf(precoProduto));
                campoQuantidade.setText("1");//set como padrão a quantidade produto
                CampoDesconto.setText("00.00");//set como padrão desconto = 0

                conex.desconecta();
            } catch (SQLException ex) {
                JOptionPane.showInternalMessageDialog(rootPane, "Erro ao pesquisar preço!");
            }

        }
    }//GEN-LAST:event_tabelaPesquisaMouseClicked

