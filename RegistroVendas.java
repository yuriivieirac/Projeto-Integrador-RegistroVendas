    public void SomaProduto() {
        total = 0;
        int qtd = 0;
        float valor = 0;
        conex.execultaSql("select * from itens_venda_produto inner join tbprodutos on"
                + " itens_venda_produto.id_produto = tbprodutos.id_produto"
                + " where id_venda =" + codigovenda);

        try {
            // conex.rs.first();
            while (conex.rs.next()) {
                qtd = conex.rs.getInt("quantidade");
                valor = conex.rs.getFloat("preco_venda");
                total = total + conex.rs.getFloat("preco_venda") * conex.rs.getInt("quantidade_produto");
                //JOptionPane.showMessageDialog(null, "qtd "+qtd+" preço venda ="+valor);
            }
            CampoValorTotal.setText(String.valueOf(total - (Float.parseFloat(CampoDesconto.getText()))));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro na soma do total!\nErro:" + ex);
        }

        conex.desconecta();
    }

