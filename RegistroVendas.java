    public void preencherTabelaCliente(String Sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"Código", "Nome"};

        conex.conexão();
        conex.execultaSql(Sql);

        try {
            conex.rs.first();
            do {
                dados.add(new Object[]{conex.rs.getString("id_cliente"), conex.rs.getString("nome")});

            } while (conex.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher um ArrayList" + ex);
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);

        tabelaPesquisa.setModel(modelo);
        tabelaPesquisa.getColumnModel().getColumn(0).setPreferredWidth(150);//largura
        tabelaPesquisa.getColumnModel().getColumn(0).setResizable(false);

        tabelaPesquisa.getColumnModel().getColumn(1).setPreferredWidth(558);
        tabelaPesquisa.getColumnModel().getColumn(1).setResizable(false);

        tabelaPesquisa.getTableHeader().setResizingAllowed(false);
        tabelaPesquisa.setAutoResizeMode(tabelaPesquisa.AUTO_RESIZE_OFF);
        tabelaPesquisa.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        conex.desconecta();
    }

