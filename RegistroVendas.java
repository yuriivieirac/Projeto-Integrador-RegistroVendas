    private void buttonFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonFinalizarActionPerformed

        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");

        try {
            Date date = formatter.parse(campoData.getText());
            mod.setData(date);
        } catch (ParseException e) {
            mod.setData(new Date());
        }

        mod.setIdvenda(codigovenda);
        mod.setNomeCliente(CampoCliente.getText());
        mod.setValorDesconto(Float.parseFloat(CampoDesconto.getText()));
        mod.setValorVenda(Float.parseFloat(CampoValorTotal.getText()));
        mod.setFormaPagamento((String) campoPagamento.getSelectedItem());
        control.FecharVenda(mod);
        dispose();


    }//GEN-LAST:event_buttonFinalizarActionPerformed

